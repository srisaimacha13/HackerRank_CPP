#include <iostream>
#include <cstdio>
using namespace std;

int main() {
    char ch;
    int a,b;
    long long int c;
    double d,e;
    scanf("%d %d %lld %c %lf %lf",&a,&b,&c,&ch,&d,&e);
    printf("%d\n",a);
    printf("%d\n",b);
    printf("%lld\n",c);
    printf("%c\n",ch);
    printf("%lf\n",d);
    printf("%lf\n",e);
    return 0;
}

